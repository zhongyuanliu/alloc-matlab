function Ce = circle_move(Ce,Nc)
% input -pi to pi, output 0 to 2pi
temp = Ce(1:Nc,2);
% temp = fliplr(temp')';

Ce(1:end-Nc,2) = Ce(Nc + 1:end,2);
Ce(end - Nc + 1 : end,2) = temp;

Ce(:,1) = to2Pi(Ce(:,1));%Ce from 0 to 2pi
Ce(end + 1,1) = 0;
Ce(end,2) = Ce(1,2);
end

function Ce = circle_sym(Ce)
% symmetrize
% input -pi to pi, output 0 to 2pi

% temp = fliplr(temp')';
Ce(:,1) = -Ce(:,1);

Ce(:,1) = to2Pi(Ce(:,1));%Ce from 0 to 2pi
[Ce(:,1),pos] = sort(Ce(:,1));
Ce(:,2) = Ce(pos,2);
Ce(end + 1,1) = 0;
Ce(end,2) = Ce(1,2);
end